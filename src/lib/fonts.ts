// Performance Cache for font styles
const fontCache: Record<string, Record<string, string>> = {};

// Helper function to create character mappers
const createMapper = (charMap: Record<string, string>) => {
  return (text: string): string => {
    // Generate a cache key
    const cacheKey = JSON.stringify(charMap);
    if (!fontCache[cacheKey]) fontCache[cacheKey] = {};
    if (fontCache[cacheKey][text]) return fontCache[cacheKey][text];

    const result = text
      .split('')
      .map((char) => charMap[char] || char)
      .join('');

    fontCache[cacheKey][text] = result;
    return result;
  };
};

