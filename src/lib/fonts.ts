function zalgoChar(c: string, i: number): string {
  if (c === ' ' || c.length === 0) return c;
  let out = c;
  const seed = (c.charCodeAt(0) + i * 31) % 1000;
  const nUp = 1 + (seed % 2);
  const nDown = 1 + ((seed * 7) % 2);
  for (let j = 0; j < nUp; j++) out += String.fromCodePoint(zalgoUp[(seed + j * 17) % zalgoUp.length]);
  for (let j = 0; j < nDown; j++) out += String.fromCodePoint(zalgoDown[(seed + j * 13) % zalgoDown.length]);
  return out;
}
